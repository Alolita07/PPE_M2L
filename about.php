                    <div class="content">
                        <div class="inner" style="padding-top: 3%">
                            <header class="major">
                                <h3>Services proposés aux ligues par la M2L</h3>
                            </header>
                            <p>

                                Accès Internet
                                Les ligues disposent d'un accès Internet mutualisé que la M2L loue à un prestataire extérieur.
                                Accès Wifi
                                Dans tous les espaces, un réseau Wifi "visiteurs" est disponible, avec une clé WPA renouvelée régulièrement et
                                communiquée aux ligues. Ce réseau ne permet que l'accès à Internet.
                                Téléphonie
                                Dans les bâtiments anciens A et B, les salles et bureaux sont équipés de prises de téléphone analogiques. La
                                M2L y fournit les combinés téléphoniques. Dans les bâtiments neufs C et D, l'équipement téléphonique est de
                                type VoIP. La M2L loue des postes téléphoniques IP aux ligues.
                                Affranchissement
                                Une machine à affranchir permet un affranchissement rapide et en nombre. Cette prestation est facturée aux
                                ligues au coût de l'affranchissement. Chaque mois, on relie la machine à affranchir à une imprimante pour
                                obtenir une liste de codes de gestion correspondant aux ligues associés à une quantité et un type
                                d'affranchissement. La prise en compte de ces informations permet au CROSL d'éditer des factures.
                            </p>
                            <p>
                                <h4>Impressions en volume et en qualité imprimerie</h4>
                                Les ligues disposent de la possibilité d'imprimer sur des ressources d'impression numériques connectées
                                situées dans le local reprographie du rez-de-chaussée dont l'usage fait l'objet d'une facturation à prix coûtant.
                                Un système de comptage situé sur le serveur d'impression permet au CROSL d’effectuer une facturation
                                mensuelle auprès des ligues. <br>
                                une photocopieuse noir et blanc à 70 pages/minute avec différents dispositifs de finition,
                                une imprimante Laser couleur A4/A3 à encre solide à 25 pages/minute,
                                un traceur A2 (1 page / minute) utilisé pour les affiches et banderoles. <br> <br>
                                <h4>Serveur FTP documentaire</h4>
                                La M2L met à disposition des ligues un serveur FTP documentaire intranet/internet regroupant des textes
                                légaux, des modèles de dossiers, de statuts, des programmes de formation (...) compilés par le CROSL. <br> <br>
                                <h4>Système de réservation des salles</h4>
                                La M2L met à disposition des ligues un site web de réservation des salles (réunions, amphithéâtre,
                                restauration). Ce site est accessible en intranet, mais aussi depuis l'internet. Les réservations payantes sont
                                facturées par la Région aux utilisateurs. L’administration de la M2L lui communique les informations
                                nécessaires à cette facturation de façon hebdomadaire.
                            </p>
                            <p>
                                <h4>Information sur le digicode du jour et la clé Wifi</h4>
                                La M2L met à disposition des ligues un site web d'information sur le digicode permettant l'accès à la M2L ainsi
                                que sur la clé Wifi "visiteurs". Le système de réservation donne également le digicode du jour dans le compterendu de réservation envoyé automatiquement par mail. <br><br>
                                <h4>Système de gestion des configurations</h4> <br>
                                M2L gère à travers un logiciel de gestion des configurations l'ensemble du parc informatique incluant les postes
                                fixes des ligues. <br><br>
                                <h4>Intégration des postes informatiques des ligues</h4>
                                Lorsque les ligues acquièrent du matériel informatique, il y a une phase obligatoire d'intégration qui consiste à : <br>
                                <li>installer un antivirus affilié au serveur antiviral de la M2L,</li>
                                <li>installer la dernière version de l'agent qui réalise l’inventaire matériel et logiciel
                                    paramétrer le poste en adressage IP automatique,</li>
                                <li>installer un système de sauvegarde de données sur un site FTP de sauvegarde géré par la M2L,</li>
                                <li>effectuer les dernières mises à jour systèmes et à paramétrer leur automatisation,</li>
                                paramétrer les noms des postes selon les règles de gestion suivantes : <br>
                                B[code bâtiment]E[numéro étage]L[numéro ligue]S[numéro salle].P[numéro poste] <br>
                                Code bâtiment qui peut être A ou C <br>
                                Numéro étage est compris entre 1 et 4 (puisque les locaux du rez-de-chaussée n'hébergent pas de
                                ligues) <br>
                                Numéro ligue correspond à un nombre attribué à la ligue allant pour l'instant de 1 à 24 <br>
                                Numéro salle correspond aux bureaux occupés par les ligues <br>
                                Numéro poste correspond au numéro écrit sur la prise murale <br>
                                Exemple : le nom d'hôte BAE2L6S01P01 correspond au poste installé sur la prise N°1 du bureau A201 occupé
                                par la ligue de Volley, bureau situé au deuxième étage du bâtiment A. Cette intégration est contractualisée. Les
                                ligues et CD étant toutes des structures associatives indépendantes, leurs postes ne sont pas intégrés dans un
                                annuaire central. Par contre, les postes de l'administration de la M2L et de la salle multimédia le sont.
                            </p>
                            <p>
                                <h4>Intégration d'imprimantes</h4>
                                Lorsque les structures hébergées s'équipent d'imprimante réseau, la connexion en est réalisée par
                                l'informaticien bénévole du CROSL, sans passer par un serveur d'impression. Le nom de l'imprimante est lui
                                aussi codifié de la même façon que celui des postes (la lettre I vient remplacer la lettre P).<br><br>
                                <h4>Service d'établissement de bulletins de salaire</h4>
                                Le CRIB (Centre Régional d'Information des Bénévoles) est un label donné au CROSL qui, entre autres missions
                                d'information (sur les textes réglementaires, la convention nationale du sport ...), propose un service
                                d'établissement de bulletins de salaires aux ligues et à leurs clubs affiliés. Le CROSL est tiers de confiance pour l'URSSAF et, à ce titre, établit des bulletins de salaires réglementaires et tous les documents annexes. La
                                prestation est facturée au forfait (60 € par an) et au bulletin édité (5 €). Un employé du CROSL est affecté à
                                cette mission à raison de 0,8 ETP.
                            </p>
                            <p>
                                Illustration du processus pour une ligue qui fait établir ses bulletins de salaires par le CRIB, pour des animateurs non permanents, ou pour ses permanents. <br>
                                <li>La ligue établit une Déclaration Unique d'Embauche et un contrat de travail.</li>
                                <li>Nom, prénom, date de naissance, adresse, n° de sécu, de l’intervenant ou du salarié</li>
                                <li>Dates et heures d'intervention (de jour / de nuit)</li>
                                <li>Heures congés payées associées</li>
                                <li>Taux horaire</li>
                                <li>Intitulé dans la grille de la convention nationale du sport (ex. technicien niveau 3)</li>
                                En retour, le CRIB fournit le bulletin de salaire et le document sur les charges périodiques.
                                Le salaire est viré par la ligue, si celle-ci a fait ce choix. Sinon il y a un prélèvement sur son compte et virement sur le compte du salarié.<br>
                                Pour les versements aux organismes sociaux, il y a prélèvement direct sur le compte de la ligue.<br><br>
                                <h4>Formations</h4><br>
                                Le CROSL offre un catalogue diversifié de formations aux bénévoles des clubs affiliés aux ligues (législation,
                                éthique, comptabilité associative, etc.). Les ligues organisent également des formations, en général plus
                                techniques, sur l'usage de logiciels spécifiques de gestion des clubs ou des compétitions sportives
                            </p>
                        </div>
